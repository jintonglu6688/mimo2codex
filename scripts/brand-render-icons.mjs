// Generates tray + app icons from two SVG sources:
// - package/brand/tray.svg  → tray icons (16/32/48). Simplified design, no
//   gradient, big bold "m2" — readable at system-tray size.
// - package/brand/logo.svg  → app icons (16/32/48/64/128/256). Full m2c logo
//   with gradient, used in Explorer, installer, taskbar, About dialog.
//
// Outputs:
// - Mac: tray-Template.png + @2x (black silhouette), icon.icns (app)
// - Win: tray.ico (small, simplified), icon.ico (multi-size, full logo)
// Run: npm run brand:icons
import { execSync } from "node:child_process";
import { writeFileSync, mkdirSync, readFileSync, rmSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const brandLogoSvg = resolve(root, "package/brand/logo.svg");
const brandTraySvg = resolve(root, "package/brand/tray.svg");
const brandPng1024 = resolve(root, "package/brand/logo-1024.png");

mkdirSync(resolve(root, "package/win"), { recursive: true });
mkdirSync(resolve(root, "package/mac"), { recursive: true });

// ── Mac tray template (monochrome silhouette of the SIMPLIFIED tray icon) ──
const traySvg = readFileSync(brandTraySvg, "utf8");
const trayMonoSvg = traySvg
  .replace(/fill="#4F6CFB"/g, 'fill="none"')
  .replace(/fill="#FFFFFF"/g, 'fill="#000000"');
const trayMonoPath = resolve(root, ".tmp-tray-mono.svg");
writeFileSync(trayMonoPath, trayMonoSvg, "utf8");

execSync(`npx --yes sharp-cli@4 -i .tmp-tray-mono.svg -o package/mac/tray-Template.png resize 32 32`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i .tmp-tray-mono.svg -o package/mac/tray-Template@2x.png resize 64 64`, { stdio: "inherit", cwd: root });

// ── Win tray.ico (small, from SIMPLIFIED tray.svg) ─────────────────────────
execSync(`npx --yes sharp-cli@4 -i package/brand/tray.svg -o .tmp-tray-16.png resize 16 16`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/tray.svg -o .tmp-tray-32.png resize 32 32`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/tray.svg -o .tmp-tray-48.png resize 48 48`, { stdio: "inherit", cwd: root });
execSync(`npx --yes png-to-ico@2 .tmp-tray-16.png .tmp-tray-32.png .tmp-tray-48.png > package/win/tray.ico`, { stdio: "inherit", cwd: root, shell: true });

// ── Win icon.ico (app icon, multi-size from FULL logo.svg) ─────────────────
execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o .tmp-app-16.png resize 16 16`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o .tmp-app-32.png resize 32 32`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o .tmp-app-48.png resize 48 48`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o .tmp-app-64.png resize 64 64`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o .tmp-app-128.png resize 128 128`, { stdio: "inherit", cwd: root });
execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o .tmp-app-256.png resize 256 256`, { stdio: "inherit", cwd: root });
execSync(`npx --yes png-to-ico@2 .tmp-app-16.png .tmp-app-32.png .tmp-app-48.png .tmp-app-64.png .tmp-app-128.png .tmp-app-256.png > package/win/icon.ico`, { stdio: "inherit", cwd: root, shell: true });

// Cleanup temp files
const tmpFiles = [
  ".tmp-tray-mono.svg",
  ".tmp-tray-16.png", ".tmp-tray-32.png", ".tmp-tray-48.png",
  ".tmp-app-16.png", ".tmp-app-32.png", ".tmp-app-48.png",
  ".tmp-app-64.png", ".tmp-app-128.png", ".tmp-app-256.png",
];
for (const f of tmpFiles) {
  try { rmSync(resolve(root, f), { force: true }); } catch { /* best-effort */ }
}

void brandLogoSvg;  // Silence "declared but not used" — referenced via path string above

// Mac app icon (.icns) — cross-platform generation using png2icons (pure JS).
// We use logo-1024.png as input (it must exist; generated by `npm run brand:render`
// or auto-rendered here if missing).
if (!existsSync(brandPng1024)) {
  console.log("[icons] logo-1024.png missing; running brand:render first...");
  execSync(`npx --yes sharp-cli@4 -i package/brand/logo.svg -o package/brand/logo-1024.png resize 1024 1024 --withoutEnlargement=false`, { cwd: root, stdio: "inherit" });
}

console.log("[icons] generating package/mac/icon.icns via png2icons...");
// png2icons CLI: png2icons <input> <output-prefix> -icns
// It writes <output-prefix>.icns. We pass a tmp prefix and rename to final path.
const icnsTmp = resolve(root, "package/mac/.tmp-icon");
try {
  execSync(`npx --yes png2icons@2 "${brandPng1024}" "${icnsTmp}" -icns -bc`, { cwd: root, stdio: "inherit" });
  // png2icons output is <prefix>.icns
  execSync(process.platform === "win32"
    ? `move /Y "${icnsTmp}.icns" "${resolve(root, "package/mac/icon.icns")}"`
    : `mv -f "${icnsTmp}.icns" "${resolve(root, "package/mac/icon.icns")}"`,
    { cwd: root, stdio: "inherit", shell: true });
} catch (err) {
  console.warn("[icons] WARN: icon.icns generation failed:", err.message);
  console.warn("[icons] Mac packaging will fall back to logo-1024.png if needed.");
}

console.log("brand icons generated:");
console.log("  package/win/tray.ico");
console.log("  package/win/icon.ico");
console.log("  package/mac/tray-Template.png + @2x");
console.log("  package/mac/icon.icns");
